/// Spacing tokens built on a 4 px base grid.
///
/// Named after T-shirt sizes up to [xl], then [x2l]...[x36l] for
/// the extended scale. Use these constants everywhere instead of
/// raw numeric literals.
abstract final class SpacingTokens {
  /// 0 px — no space.
  static const double none = 0;

  /// 1 px — hairline / 1-pixel rule.
  static const double px = 1;

  /// 2 px.
  static const double xs = 2;

  /// 4 px — base unit.
  static const double sm = 4;

  /// 8 px.
  static const double md = 8;

  /// 12 px.
  static const double lg = 12;

  /// 16 px.
  static const double xl = 16;

  /// 20 px.
  static const double x2l = 20;

  /// 24 px.
  static const double x3l = 24;

  /// 32 px.
  static const double x4l = 32;

  /// 40 px.
  static const double x5l = 40;

  /// 48 px.
  static const double x6l = 48;

  /// 56 px.
  static const double x7l = 56;

  /// 64 px.
  static const double x8l = 64;

  /// 80 px.
  static const double x9l = 80;

  /// 96 px.
  static const double x10l = 96;

  /// 112 px.
  static const double x11l = 112;

  /// 128 px.
  static const double x12l = 128;

  /// 160 px.
  static const double x14l = 160;

  /// 192 px.
  static const double x16l = 192;

  /// 256 px.
  static const double x20l = 256;

  /// 320 px.
  static const double x24l = 320;

  /// 384 px.
  static const double x28l = 384;

  /// 448 px.
  static const double x32l = 448;

  /// 512 px.
  static const double x36l = 512;
}
